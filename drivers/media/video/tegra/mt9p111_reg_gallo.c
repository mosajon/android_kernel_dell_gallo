#define EEPROM_SLAVEADDR (0xA0 >> 1)
#define EEPROM_LC_LENGTH (0x66)
#define EEPROM_BLOCKSIZE_1 (20)
#define EEPROM_BLOCKSIZE_2 (EEPROM_LC_LENGTH * 2)
#define EEPROM_BLOCKSIZE_3 (12)
#define EEPROM_BLOCKSIZE_4 (4)
#define EEPROM_BLOCKSIZE_5 (4)

static int sensor_read_eeprom_block(struct i2c_client *client, u16 block_size,
					u8 *data)
{
	int err;
	/*int i;*/
	struct i2c_msg msg;

	if (!client->adapter)
		return -ENODEV;

	pr_info("mt9p111 %s ++\n", __func__);

	msg.addr = EEPROM_SLAVEADDR;
	msg.flags = I2C_M_RD;
	msg.len = block_size;
	msg.buf = data;

	err = i2c_transfer(client->adapter, &msg, 1);

	if (err != 1)
		return -EINVAL;
/*
	for (i = 0; i < block_size; i++)
		pr_info("0x%02X\n", data[i]);
*/
	return 0;
}

static int sensor_parsing_data(struct sensor_reg_v2 table[], u16 block_size,
				u8 *data)
{
	int i = 0;
	struct sensor_reg_v2 *next;
	for (next = table; next->purpose != CMD_TABLE_END; next++) {
		if (next->purpose == CMD_REG16) {
			i += 2;
			if (i > block_size)
				break;
			next->val = (data[i - 1] << 8) | data[i - 2];
			/*pr_info("0x%04X, 0x%04X\n", next->addr, next->val);*/
		}
	}
	return 0;
}

static int sensor_parsing_data_pga(struct sensor_reg_v2 table[], u32 block_name,
				u16 block_size, u8 *data)
{
	int i = 0;
	struct sensor_reg_v2 *next;
	/*pr_info("sensor_parsing_data_pga\n");*/
	for (next = table; next->purpose != CMD_TABLE_END; next++) {
		if (next->purpose == block_name) {
			i += 2;
			if (i > block_size)
				break;
			next->val = (data[i - 1] << 8) | data[i - 2];
			/*pr_info("0x%04X, 0x%04X\n", next->addr, next->val);*/
		}
	}
	return 0;
}

